# Task 1: Break at Banana

fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    if fruit == "banana":
        break

    print(fruit)


# Task 2: Stop at Number 4

for i in range(1, 10):
    if i == 4:
        break

    print(i)


# Task 3: Break in Names

names = ["Amr", "Ali", "Ahmed"]

for name in names:
    if name == "Ali":
        break

    print(name)


# Task 4: Break Example

